public class Pair {
    String programmer1;
    String Programmer2;
    String date;

    public Pair(String programmer1, String programmer2, String date) {
        this.programmer1 = programmer1;
        Programmer2 = programmer2;
        this.date = date;
    }

    public String getProgrammer1() {
        return programmer1;
    }

    public String getProgrammer2() {
        return Programmer2;
    }

    public String getDate() {
        return date;
    }
}
